var VERSION="3.35";

function newVersionText(p) {
    var ver = GM_getValue('newversion',VERSION);
    if (ver>VERSION) {
        var v = document.createElement('br');
        p.appendChild(v);
        v = document.createElement('a');
        v.setAttribute('href','http://userscripts.org/scripts/source/85176.user.js');
        v.setAttribute('style','font-size:x-small;color:orange;vertical-align:middle;');
        v.setAttribute('title','Version '+ver+' is available.  You have version '+VERSION);
        v.setAttribute('target','_blank');
        v.appendChild(document.createTextNode('New version of Inventory Filter available!!'));
        p.appendChild(v);
    } 
}

function GM_get(dest, callback)
{   GM_xmlhttpRequest({
      method: 'GET',
      url: dest,
        //onerror:function(error) { GM_log("GM_get Error: " + error); },
        onload:function(details) {
            if( typeof(callback)=='function' ){
                callback(details.responseText);
}   }   }); }

// auto-update check. (Some code adapted from ALLCOLOR.user.js)
function checkVersion() {
    var lastUpdateCheck = GM_getValue('lastUpdateCheck','NEVER');
    var curTime = new Date().getTime();
    if ((lastUpdateCheck == 'NEVER') || (parseInt(lastUpdateCheck) < (curTime - 345600000))) { // every 4 days
        GM_setValue('lastUpdateCheck',''+curTime);
        GM_get('http://userscripts.org/scripts/source/85176.meta.js',
               function(responseText) {
                   var v = responseText.match(/@version\s*([0-9.]+)/);
                   if (v) {
                       //GM_log('found version: '+v[1]);
                       GM_setValue('newversion',v[1]); 
                   }
               });
    }
}

function addFilterBox() {
    var node = document.evaluate( '//form[@id="showing"]', document, null, XPathResult.FIRST_ORDERED_NODE_TYPE, null);
    if (!node.singleNodeValue) {
        node = document.evaluate( '//a[@href="craft.php" and text()="craft stuff"]', document, null, XPathResult.FIRST_ORDERED_NODE_TYPE, null);
    }
    if (node.singleNodeValue) {
        var p = node.singleNodeValue.parentNode;
        while (p && p.tagName!='P')
            p = p.parentNode;
        if (p) {
            var newp = document.createElement('center');
            p.appendChild(newp);
            addFilterBoxTextHTML(newp);
        }
        return;
    }
    addFilterBox2();
}

function addFilterBox2() {
    if (document.getElementById('goer'))
        return;
    var labels = document.evaluate( '//tr/td/b[text()="Crafting:"]', document, null, XPathResult.FIRST_ORDERED_NODE_TYPE,null);
    if (labels.singleNodeValue) {
        var p = labels.singleNodeValue.parentNode;
        while (p && p.tagName!='TABLE')
            p = p.parentNode;
        if (p) {
            p = p.rows[1].cells[0].firstChild;
            addFilterBoxTextHTML(p);
        }
            
    }
}

function addFilterBoxTextHTML(p) {
    var f = document.createElement('font');
    f.setAttribute('size','2');
    var tog = document.createElement('input');
    tog.setAttribute('id','invfilter_toggle');
    tog.setAttribute('type','checkbox');
    tog.setAttribute('checked','true');
    tog.setAttribute('style','vertical-align:middle;');
    tog.addEventListener("click", processCheckboxItem, true);
    f.appendChild(tog);
    var lab = document.createTextNode('Filter:');
    var link = document.createElement('input');
    link.setAttribute('id','invfilter_text');
    link.setAttribute('type','text');
    link.setAttribute('size','20');
    f.appendChild(lab);
    link.addEventListener("change", doFilter, true);
    f.appendChild(link);
    
    tog = document.createElement('input');
    tog.setAttribute('id','ci_toggle');
    tog.setAttribute('type','checkbox');
    if (GM_getValue('casesensitive','true'))
        tog.setAttribute('checked','true');
    tog.addEventListener("click", processCheckboxItem, true);
    tog.setAttribute('style','vertical-align:middle;');
    
    var ci = document.createTextNode('\u00A0\u00A0(Case-sensitive:');
    f.appendChild(ci);
    f.appendChild(tog);
    ci = document.createTextNode(')');
    f.appendChild(ci);

    p.appendChild(f);
    
    newVersionText(p);
}

function processCheckboxItem() {
    var setting;
    if (!this.getAttribute('checked')) {
        setting = 'true';
        this.setAttribute('checked','true');
    } else {
        setting = '';
        this.removeAttribute('checked');
    }
    if (this.getAttribute('id')=='ci_toggle')
        GM_setValue('casesensitive',setting);
    doFilter();
}

function doFilter() {
    var t = document.getElementById('invfilter_text').value;
    var tog = document.getElementById('invfilter_toggle').getAttribute('checked');
    var ci = document.getElementById('ci_toggle').getAttribute('checked');
    if (!tog)
        t = '';
    var meta = t.match(/#([^#]*)$/);
    if (meta) {
        t = t.substr(0,t.length-meta[1].length-1);
        //GM_log('detected meta operator: "'+meta[1]+'" in '+t);
        meta = meta[1].split(/\s*,\s*/);
    }
    var re = new RegExp(t,'m'+((ci)?'':'i'));
    var list = document.getElementsByClassName('item');
    if (list && list.length>0) {
        for (var i=0;i<list.length;i++) {
            var item = list[i];
            if (item.tagName=='TABLE') {
                var txt = item.getElementsByTagName('B')[0].innerHTML;
                if (txt) {
                    // get rid of html
                    txt = txt.replace(/\<[^<>]*\>/g,'');
                    txt = txt.replace(/&nbsp;/g,' ');
                    txt = txt.replace(/\[\[^\[\]*\]/g,'');
                } else txt = '';
                setDisplayStyle(item.parentNode,txt,re,meta);
            }
        }
    } else {
        // discoveries page
        list = document.evaluate('//td[@valign="top"]/b', document, null, XPathResult.ORDERED_NODE_SNAPSHOT_TYPE,null);
        for (var i=list.snapshotLength-1;i>=0;i--) {
            var item = list.snapshotItem(i);
            var txt = item.innerHTML;
            if (txt) {
                // get rid of html
                txt = txt.replace(/\<[^<>]*\>/g,'');
                txt = txt.replace(/&nbsp;/g,' ');
                txt = txt.replace(/\[\[^\[\]*\]/g,'');
            } else txt = '';

            var p = item.parentNode;
            if (p.previousSibling) {
                setDisplayStyle(p.previousSibling,txt,re,meta);
            }
            setDisplayStyle(p,txt,re,meta);
        }
    }
}


function setDisplayStyle(node,txt,re,meta) {
    var s = node.getAttribute('style');
    if (!s) s = '';
    if (txt.match(re) && (!meta || (meta && checkElem(txt,meta)))) {
        s = s.replace(/display:\s*none[;]*/g,'');
        node.setAttribute('style',s);
    } else if (!s.match(/display:\s*none[;]*/)) {
        node.setAttribute('style','display:none;'+s);
    }
}

function checkElem(txt,meta) {
    var t = elems[txt];
    if (t) {
        for (m in meta) {
            if (t.indexOf(meta[m])>=0)
                return true;
        }
    }
}

