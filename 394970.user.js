/* 监视网页更新 */
$('#review').bind('DOMSubtreeModified', function () {
    setTimeout(function () {
        change();
    }, 300);
});

/* 更改词汇翻译 */
function change(){
    var word = $('h1.content').text().replace(/\[.*\]/, '').replace(/\W/g, '');
    var explanation = map[word];
    if (typeof explanation  === "undefined"){
        return;
    }
    explanation = '<pre>' + explanation.replace(/[;；]/g,'；\r\n') + '</pre>';
    var s1 = $("#review-definitions .text");
    if (!s1.children("pre")[0]) {
        s1.html(explanation);
    }
    var s2 = $("#definition-cn-hint .hint-content");
    if (!s2.children("pre")[0]) {
        s2.html(explanation);
    }
    var table = $("[id^=summary][data-id]").each(function(i){
        var item_word = $(this).children('.word').html();
        var item_explaination = map[item_word];
        $(this).children('[class^=definition]').html(item_explaination);
    });
}


