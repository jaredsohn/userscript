function hideSSII(className){
	var elems = document.getElementsByClassName(className);
	for (i in elems){
		for(j in listeSSII){ 
			if(elems[i].innerHTML != undefined && elems[i].innerHTML.indexOf(listeSSII[j]) != -1){ 
				// Si dans ce bloc on trouve le nom d'une SSII, on masque le bloc
				elems[i].style.display = 'none';
				break;
			}
		}
	}
}

