// parse the charpane info for the password hash (use as a session ID)
function getPwdHash(data){
    var pwdHash = /pwdhash \= \"(.*?)\"/i.exec(data); // the .*? is the non-greedy version of .*
	if(pwdHash)
		pwdHash = pwdHash[1];
	else
		pwdHash = "";

	return pwdHash;
}

