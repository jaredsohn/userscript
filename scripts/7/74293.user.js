var state = "";
var D = -1;
var S = -1;

function test_mission(j,s){
    if(mission[j][0] != "headline"){
        var expr1 = new RegExp(mission[j][1].replace("*","(\\S|\\s)*"),"i");
        if(dolog) log(expr1);
        if(expr1.test(s)){
            if(dolog) log("Mission's name erkannt bei "+j);
            if(mission[j][0] == "normal")
                return true;
            if(dolog) log("wastland mission");
            if(state == ""){
                var expr2 = /(Genjutsu|Ninjutsu|Taijutsu)(\S|\s)*Difficulty (\d+)&nbsp;&nbsp;&nbsp;Successes (\d+)/;
                if(expr2.exec(s) != null){
                    state = RegExp.$1;
                    D = parseInt(RegExp.$3);
                    S = parseInt(RegExp.$4);
                    if(dolog) log(state+" - D"+D+" / S"+S);
                    var expr3 = /Crank Level: (\d+)/
                    if(expr3.exec(s) != null){
                        if(dolog) log("Crank erkannt : "+parseInt(RegExp.$1));
                        D -= parseInt(RegExp.$1);
                        S -= parseInt(RegExp.$1);
                        if(dolog) log(state+" - D"+D+" / S"+S);
                    }
                }
            }
            switch(state){
                case "Genjutsu":
                    if((D == mission[j][2])&&(S == mission[j][3])) return true;
                    break
                case "Ninjutsu":
                    if((D == mission[j][4])&&(S == mission[j][5])) return true;
                    break
                case "Taijutsu":
                    if((D == mission[j][6])&&(S == mission[j][7])) return true;
                    break
            }
        }
    }
    return false;
}

